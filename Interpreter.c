//---------------------OutCRLF---------------------
// Output a CR,LF to UART to go to a new line
// Input: none
// Output: none
void OutCRLF(void){
  UART_OutChar(CR);
  UART_OutChar(LF);
}

// *********** Command line interpreter (shell) ************
void Interpreter(void){ 
  // write this  
	char i;
  char string[20];  // global to assist in debugging
  uint32_t n;
	
	OutCRLF();
  for(i='A'; i<='Z'; i=i+1){// print the uppercase alphabet
    UART_OutChar(i);
  }
  OutCRLF();
  UART_OutChar(' ');
  for(i='a'; i<='z'; i=i+1){// print the lowercase alphabet
    UART_OutChar(i);
  }
  OutCRLF();
  UART_OutChar('-');
  UART_OutChar('-');
  UART_OutChar('>');
  while(1){
    UART_OutString("InString: ");
    UART_InString(string,19);
    UART_OutString(" OutString="); 
		UART_OutString(string); 
		OutCRLF();

    UART_OutString("InUDec: ");  
		n=UART_InUDec();
    UART_OutString(" OutUDec="); 
		UART_OutUDec(n); 
		OutCRLF();

    UART_OutString("InUHex: ");  
		n=UART_InUHex();
    UART_OutString(" OutUHex="); 
		UART_OutUHex(n); 
		OutCRLF();
  }
}


